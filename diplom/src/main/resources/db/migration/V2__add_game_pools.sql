-- Создание таблицы игровых пулов
CREATE TABLE game_pools (
    id BIGSERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    description TEXT,
    theme VARCHAR(100) NOT NULL, -- тематика пула (история, наука, спорт и т.д.)
    difficulty_modifier DECIMAL(3,2) DEFAULT 1.0, -- модификатор сложности для пула
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Добавление связи вопросов с пулами
ALTER TABLE questions ADD COLUMN pool_id BIGINT REFERENCES game_pools(id);

-- Создание таблицы для отслеживания пройденных пулов пользователями
CREATE TABLE user_completed_pools (
    id BIGSERIAL PRIMARY KEY,
    user_id BIGINT NOT NULL REFERENCES users(id),
    pool_id BIGINT NOT NULL REFERENCES game_pools(id),
    completed_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    final_prize_amount INTEGER DEFAULT 0,
    questions_answered INTEGER DEFAULT 0,
    UNIQUE(user_id, pool_id)
);

-- Добавление связи игр с пулами
ALTER TABLE games ADD COLUMN pool_id BIGINT REFERENCES game_pools(id);

-- Индексы для оптимизации
CREATE INDEX idx_questions_pool_id ON questions(pool_id);
CREATE INDEX idx_questions_pool_difficulty ON questions(pool_id, difficulty_level);
CREATE INDEX idx_user_completed_pools_user_id ON user_completed_pools(user_id);
CREATE INDEX idx_user_completed_pools_pool_id ON user_completed_pools(pool_id);
CREATE INDEX idx_games_pool_id ON games(pool_id);

